import { Action, ActionPanel, AI, Detail, Icon, Toast, environment, showToast } from "@raycast/api";
import { useEffect, useState } from "react";
import { getErrorMessage } from "../utils";

interface ComparableQuoteProps {
  chapter: number;
  verse: number;
  sanskrit: string;
  translation: string;
  language: string;
  targetScripture: "Bible" | "Quran" | "Torah" | "Dhammapada";
}

const ACCURACY_DISCLAIMER = "AI-generated comparisons may not be 100% accurate; verify with primary scripture sources.";

function buildComparablePrompt({
  chapter,
  verse,
  sanskrit,
  translation,
  language,
  targetScripture,
}: ComparableQuoteProps): string {
  return [
    "You are a comparative religion assistant.",
    `Find ONE comparable quote for Bhagavad Gita ${chapter}:${verse}.`,
    "",
    "Bhagavad Gita verse context:",
    `- Sanskrit: "${sanskrit}"`,
    `- Translation: "${translation}"`,
    "",
    "Target scripture:",
    `- ${targetScripture}`,
    "",
    "Rules:",
    "- Pick the single strongest thematic match from the target scripture only.",
    "- Include an exact citation when known (e.g., Book Chapter:Verse or Surah:Ayah).",
    "- If exact wording/citation is uncertain, explicitly mark citation as approximate.",
    "- Do not invent certainty. Prefer honesty over precision.",
    `- Write the full response in ${language}.`,
    "",
    "Return markdown exactly with these sections:",
    "## Comparable Quote",
    "## Source",
    "## Why It Compares",
    "## Confidence/Disclaimer",
  ].join("\n");
}

export function ComparableQuote(props: ComparableQuoteProps) {
  const { chapter, verse, sanskrit, translation, language, targetScripture } = props;
  const [data, setData] = useState("");
  const [isLoading, setIsLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    let cancelled = false;

    async function fetchComparableQuote() {
      if (!environment.canAccess(AI)) {
        if (!cancelled) {
          setError(
            "Raycast AI is not available. You may need Raycast Pro, or AI may not be supported on your platform yet.",
          );
          setIsLoading(false);
        }
        return;
      }

      try {
        const answer = await AI.ask(
          buildComparablePrompt({ chapter, verse, sanskrit, translation, language, targetScripture }),
        );
        if (!cancelled) {
          setData(answer);
        }
      } catch (err: unknown) {
        if (!cancelled) {
          const message = getErrorMessage(err);
          setError(`Failed to generate comparable quote: ${message}`);
          showToast({
            style: Toast.Style.Failure,
            title: "Comparable Quote Unavailable",
            message,
          });
        }
      } finally {
        if (!cancelled) {
          setIsLoading(false);
        }
      }
    }

    void fetchComparableQuote();
    return () => {
      cancelled = true;
    };
  }, [chapter, verse, sanskrit, translation, language, targetScripture]);

  const markdown = error
    ? error
    : `> **Disclaimer:** ${ACCURACY_DISCLAIMER}\n\n${data || "Finding a comparable scripture quote..."}`;

  return (
    <Detail
      isLoading={isLoading}
      markdown={markdown}
      navigationTitle={`${targetScripture} Comparable Quote • ${chapter}:${verse}`}
      metadata={
        !error ? (
          <Detail.Metadata>
            <Detail.Metadata.Label title="Response generated by" text="Raycast AI" icon={Icon.Stars} />
            <Detail.Metadata.Separator />
            <Detail.Metadata.Label title="Disclaimer" text={ACCURACY_DISCLAIMER} />
          </Detail.Metadata>
        ) : undefined
      }
      actions={
        <ActionPanel>
          {data && !error ? <Action.CopyToClipboard title="Copy Comparable Quote" content={data} /> : null}
        </ActionPanel>
      }
    />
  );
}
